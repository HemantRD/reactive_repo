package com.vinsys.hrms.datamodel.traveldesk;

public class VOReportTraveldeskSummary {

	private int cabRequestCount;
	private int ticketRequestCount;
	private int accomodationRequestCount;
	private int totalRequestCount;
	
	public int getCabRequestCount() {
		return cabRequestCount;
	}
	public void setCabRequestCount(int cabRequestCount) {
		this.cabRequestCount = cabRequestCount;
	}
	public int getTicketRequestCount() {
		return ticketRequestCount;
	}
	public void setTicketRequestCount(int ticketRequestCount) {
		this.ticketRequestCount = ticketRequestCount;
	}
	public int getAccomodationRequestCount() {
		return accomodationRequestCount;
	}
	public void setAccomodationRequestCount(int accomodationRequestCount) {
		this.accomodationRequestCount = accomodationRequestCount;
	}
	public int getTotalRequestCount() {
		return totalRequestCount;
	}
	public void setTotalRequestCount(int totalRequestCount) {
		this.totalRequestCount = totalRequestCount;
	}
	
}