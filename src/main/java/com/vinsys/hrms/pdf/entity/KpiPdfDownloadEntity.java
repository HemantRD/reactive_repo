package com.vinsys.hrms.pdf.entity;

public class KpiPdfDownloadEntity {

	    private String subCategory;
	    private String goalTitle;
	    private String goalWeight;
	    private String metric;
	    private String selfRating;
	    private String selfComments;
	    private String managerRating;
	    private String managerComments;

	    // Getters and Setters
	    public String getSubCategory() { return subCategory; }
	    public void setSubCategory(String subCategory) { this.subCategory = subCategory; }

	    public String getGoalTitle() { return goalTitle; }
	    public void setGoalTitle(String goalTitle) { this.goalTitle = goalTitle; }

	    public String getGoalWeight() { return goalWeight; }
	    public void setGoalWeight(String goalWeight) { this.goalWeight = goalWeight; }

	    public String getMetric() { return metric; }
	    public void setMetric(String metric) { this.metric = metric; }

	    public String getSelfRating() { return selfRating; }
	    public void setSelfRating(String selfRating) { this.selfRating = selfRating; }

	    public String getSelfComments() { return selfComments; }
	    public void setSelfComments(String selfComments) { this.selfComments = selfComments; }

	    public String getManagerRating() { return managerRating; }
	    public void setManagerRating(String managerRating) { this.managerRating = managerRating; }

	    public String getManagerComments() { return managerComments; }
	    public void setManagerComments(String managerComments) { this.managerComments = managerComments; }
	


}
