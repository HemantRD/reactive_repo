package com.vinsys.hrms.master.vo;

public class ReportingOfficerVO {
	private Long id;
	private Long reportingOfficerEmployeeId;
	private String reportingOfficerName;
	private Long departmentId;
	private String departmentName;
	public Long getId() {
		return id;
	}
	public void setId(Long id) {
		this.id = id;
	}
	public Long getReportingOfficerEmployeeId() {
		return reportingOfficerEmployeeId;
	}
	public void setReportingOfficerEmployeeId(Long reportingOfficerEmployeeId) {
		this.reportingOfficerEmployeeId = reportingOfficerEmployeeId;
	}
	public String getReportingOfficerName() {
		return reportingOfficerName;
	}
	public void setReportingOfficerName(String reportingOfficerName) {
		this.reportingOfficerName = reportingOfficerName;
	}
	public Long getDepartmentId() {
		return departmentId;
	}
	public void setDepartmentId(Long departmentId) {
		this.departmentId = departmentId;
	}
	public String getDepartmentName() {
		return departmentName;
	}
	public void setDepartmentName(String departmentName) {
		this.departmentName = departmentName;
	}

	
	
	
}
